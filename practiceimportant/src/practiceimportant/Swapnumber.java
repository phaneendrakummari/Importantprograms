package practiceimportant;

public class Swapnumber {

	public static void main(String[] args) {
		
		
	// interchanging of numbers
		
		int a=15,b=32;
		System.out.println("Original numbers :" +a+"   "+b);  
		
		// creating third variable
		 int v=a;
		 a=b;
		 b=v;
		 
		 System.out.println("after swap numbers :" +a+"   "+b);  

	}

}
